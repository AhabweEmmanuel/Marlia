package org.telegram.messenger;
import org.telegram.tgnet.TLRPC;

public class MarliaServiceUpdate {

    public static void setTimeout(Runnable runnable, int delay){
        new Thread(() -> {
            try {
                Thread.sleep(delay);
                runnable.run();
            }
            catch (Exception e){
                System.err.println(e);
            }
        }).start();
    }

}
